        <p>
            <strong>Beispieltext:</strong> <em>Ich bin dai Freund*in von Leo. Hen ist meint beste Freund*in. Als hen 17
                war, hat hen sich mir gegenüber als nichtbinär geoutet, noch bevor hen hens Eltern davon erzählt hat.
                Ich bin sehr froh darüber, dass ich meintem Freund*in bei diesem Prozess helfen konnte und ich dadurch
                jetzt ein besonders vertrauensvolles Verhältnis zu hem habe.</em>
        </p>

        <p>
            <strong>Ausnahme: Anrede</strong>
        </p>

        <p>
            Bei direkter Anrede von nichtbinären/genderqueeren/agender Personen empfehlen wir, nicht die
            Adjektivendungen zu übernehmen, da sonst der Anschein einer weiblichen Form erweckt wird. Deshalb verwenden
            wir hierfür das Gendersternchen nach dem Wortstamm (ohne Endung).
        </p>

        <p>
            Beispiel: <em>Lieb* Leo,… Sehr geehrt* Leo Musterenby,…</em>
        </p>

