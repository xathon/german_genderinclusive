            progress“ und wird laufend ergänzt. Das NoNa-System soll als Vorschlag und Inspiration für andere
            nichtbinäre/genderqueere/agender Personen dienen und erhebt keinen Anspruch auf Allgemeingültigkeit. Mehr
            Infos und genaue Erklärungen zum NoNa-System und geschlechtsneutraler Sprache findest du unter <a
                href="http://www.geschlechtsneutralesdeutsch.com" class="urlextern"
                title="http://www.geschlechtsneutralesdeutsch.com"
