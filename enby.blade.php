    <h2 class="sectionedit34" id="endung_mit_-el">Endung mit -el</h2>
    <div class="level2">

        <p>
            Man kann Substantive geschlechtsneutral machen, indem man statt dem femininen <strong>-in</strong> ein
            <strong>-el</strong> ans Wort hängt.
        </p>

        <p>
            Beispiel:
        </p>

        <p>
            Schüler/Schülerin → Schüler<strong>el</strong>
            Bürger/Bürgerin → Bürger<strong>el</strong>
        </p>

        <p>
            Bei Wörtern, bei dem sich der Wortstamm beim Hinzufügen des femininen Suffixes ändert (wie Arzt → Ärztin,
            Prinz → Prinzessin), kann man sich aussuchen, wie man verfährt.
        </p>

        <p>
            Arzt/Ärztin → Arzt<strong>el</strong>/Ärzt<strong>el</strong>
            Prinz/Prinzessin → Prinz<strong>el</strong>/Prinz<strong>essel</strong>
        </p>

        <p>
            Solche Wörter werden mit dem Artikel <strong>das</strong> verwendet, außerdem bleibt der Plural gleich.
        </p>

        <p>
            Das Lehrerel unterrichtet die Schülerel. (außer natürlich im Dativ: Das Lehrerel gibt den Schülereln ihre
            Hausaufgaben.)
        </p>

        <p>
            Ähnlich funktioniert das von <a href="http://www.luisepusch.de" class="urlextern"
                                            title="http://www.luisepusch.de" rel="nofollow">Luise Pusch</a> <a
                href="https://block-magazin.de/mein-freundil-francis-und-die-grenzen-der-deutschen-grammatik/"
                class="urlextern"
                title="https://block-magazin.de/mein-freundil-francis-und-die-grenzen-der-deutschen-grammatik/"
                rel="nofollow">vorgeschlagene</a> Suffix <strong>-il</strong>; als Plural kann dann -ille dienen (Das
            Lehreril unterrichtet die Schülerille. Das Lehreril gibt den Schülerillen ihre Hausaufgaben.). Das Suffix
            -il ist speziell für nichtbinäre Personen gedacht, nicht als generische Form, die für alle Menschen passen
            würde.
        </p>

    </div>

    <h2 class="sectionedit35" id="kurzformen_und_nonsenswörter">Kurzformen und Nonsenswörter</h2>
    <div class="level2">

        <p>
            <sup><a href="#fn__8" id="fnt__8" class="fn_top">8)</a></sup>
            Relativ bekannte Kurzformen auf -i, als Artikel di:er, d*, dx oder ähnliches:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> der_die Studi, dix Touri</div>
            </li>
        </ul>

        <p>
            Neue Wörter nach dem selben Muster mit -i:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> dier Mitbewohni, der*die Kollegi</div>
            </li>
        </ul>

        <p>
            -i oder ähnliche Endungen, mit „das“ als Artikel um die komplizierteren Formen zu vermeiden:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> mein bestes Freundi</div>
            </li>
            <li class="level1">
                <div class="li"> mein liebes Mitbewohnsi</div>
            </li>
            <li class="level1">
                <div class="li"> das genervte Studi</div>
            </li>
            <li class="level1">
                <div class="li"> das Touri sucht seinen Stadtplan</div>
            </li>
        </ul>

        <p>
            -mensch, -menschi, -person, -wesen verwenden:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> das Postmenschi</div>
            </li>
            <li class="level1">
                <div class="li"> meine LiebhabPerson</div>
            </li>
            <li class="level1">
                <div class="li"> der liebe Katzenbetreu-Mensch</div>
            </li>
            <li class="level1">
                <div class="li"> das Postbotwesen (oder - wenn bekannt oder deutlicher Kontext - auf eine Silbe
                    zusammengezogen: „Postbotwe&#039;n“)
                </div>
            </li>
        </ul>

        <p>
            Oder einfach irgendwelche Wörter, die man süß findet.
        </p>
        <ul>
            <li class="level1">
                <div class="li"> Ich will Fotografierflausch werden.</div>
            </li>
        </ul>

    </div>

    <h2 class="sectionedit36" id="strategien_für_berufsbezeichnungen">Strategien für Berufsbezeichnungen</h2>
    <div class="level2">
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -er(in): -kraft</strong></div>
            </li>
        </ul>

        <p>
            Lackierer(in): Lackierkraft
        </p>

        <p>
            Mechaniker(in): Mechanikkraft
        </p>

        <p>
            ggf. unterbleibt die Umlautung: Glasbläser(in): Glasblaskraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>manchmal idiomatischer mit -ungskraft</strong></div>
            </li>
        </ul>

        <p>
            Gestalter(in) für visuelles Marketing: Gestaltungskraft für visuelles Marketing
        </p>

        <p>
            Gebäudereiniger(in): Gebäudereinigungskraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -meister(in): -meisterkraft (d.h. das „er“ bleibt
                        erhalten)</strong></div>
            </li>
        </ul>

        <p>
            Agrarservicemeister(in): Agrarservicemeisterkraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -pfleger(in): -pflegekraft (d.h. das „e“ bleibt
                        erhalten)</strong></div>
            </li>
        </ul>

        <p>
            Krankenpfleger(in): Krankenpflegekraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen, die auf einem Fachbereichsnamen beruhen:
                        -fachkraft</strong></div>
            </li>
        </ul>

        <p>
            Germanist(in): Germanistikfachkraft
        </p>

        <p>
            Geograf(in): Geografiefachkraft
        </p>

        <p>
            Jurist(in): Jurafachkraft
        </p>

        <p>
            manchmal auch idiomatischer bei -er(in): Gärtner(in): Gartenbaufachkraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -frau/-mann: -leut (Rückbildung vom Plural)</strong>
                </div>
            </li>
        </ul>

        <p>
            Bankkauffrau/mann: (das) Bankkaufleut (wenn eins es schöner findet, geht auch Bankkaufperson)
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -ant oder -ent: lateinisches Neutrum</strong></div>
            </li>
        </ul>

        <p>
            Laborant(in): Laborans (das a von -ans ist lang! Plural: entweder Neutrum „Laborantia“ oder Maskulinum und
            Femininum „Laborantes“ (mit langem e))
        </p>

        <p>
            Korrespondent(in): Korrespondens (wieder mit langem e, nicht wie „Korrespondenz“! Plural „Korrespondentia“
            oder „Korrespondentes“, s.o.)
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>Berufsbezeichnungen auf -wirt(in): -bewirtschaftungskraft</strong></div>
            </li>
        </ul>

        <p>
            Betriebswirt(in): Betriebsbewirtschaftungskraft
        </p>

        <p>
            Forstwirt(in): Forstbewirtschaftungskraft
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>wenn alle Stricke reißen, Kollektivbezeichnung mit -glied</strong></div>
            </li>
        </ul>

        <p>
            Rechtsanwalt/Rechtsanwältin: Rechtsanwaltschaftsglied
        </p>

        <p>
            Ingenieur(in): Ingenieurwesensglied
