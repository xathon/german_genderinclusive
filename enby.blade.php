    <h2 class="sectionedit27" id="sternchen_unterstrich_satzzeichen">Sternchen, Unterstrich, Satzzeichen</h2>
    <div class="level2">

        <p>
            Ausgesprochen als kurze Pause oder <a href="https://de.wikipedia.org/wiki/Stimmloser_glottaler_Plosiv"
                                                  class="urlextern"
                                                  title="https://de.wikipedia.org/wiki/Stimmloser_glottaler_Plosiv"
                                                  rel="nofollow">Knacklaut</a>.
        </p>

        <p>
            In der bekanntesten Version wird das Zeichen zwischen die „maskuline“ und die „feminine“ Endung gesetzt:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> mein_e beste_r Freund_in</div>
            </li>
            <li class="level1">
                <div class="li"> mein*e beste*r Freund*in</div>
            </li>
            <li class="level1">
                <div class="li"> mein:e beste:r Freund:in</div>
            </li>
        </ul>

        <p>
            In einer anderen Variante nach dem Stamm - oder realistischerweise an der nächstbesten Stelle, die nichts
            mit männlichen/weiblichen Formen zu tun hat, sich aber ok aussprechen lässt:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> mei_ne bes_ter Freun_din</div>
            </li>
            <li class="level1">
                <div class="li"> mei*ne bes*ter Freun*din</div>
            </li>
            <li class="level1">
                <div class="li"> mei:ne bes:ter Freun:din</div>
            </li>
        </ul>

        <p>
            Oder an ganz zufälligen Stellen:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> mei_ne best_er Freundi_n</div>
            </li>
            <li class="level1">
                <div class="li"> mei*ne best*er Freundi*n</div>
            </li>
            <li class="level1">
                <div class="li"> mei:ne best:er Freundi:n</div>
            </li>
        </ul>

        <p>
            Es kann durchgehend eines der Zeichen verwendet werden oder mehrere vermischt.
        </p>

        <p>
            Auch &#039; und ° kommen manchmal vor:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> mein&#039;e beste&#039;r Freund&#039;in</div>
            </li>
            <li class="level1">
                <div class="li"> mein°e beste°r Freund°in</div>
            </li>
        </ul>

        <p>
            Ein Nachteil dieser Formen ist, dass sie bei der Verwendung von <a
                href="https://de.wikipedia.org/wiki/Screenreader" class="urlextern"
                title="https://de.wikipedia.org/wiki/Screenreader" rel="nofollow">Screenreadern</a> unter Umständen
            Probleme machen. <a href="/text_to_speech" class="wikilink1" title="text_to_speech">Hier wird gesammelt, wie
                verschiedene Text-to-Speech-Programme diese Lösungen wiedergeben.</a>
            <br/>
            Des Weiteren sind diese Formen nicht für alle nicht binären Menschen eine angemessene Anrede. Als
            problematisch wird angesehen, dass verschiedene nicht binäre Endungen(<span class="curid"><a
                    href="/geschlechtsneutrale_sprache" class="wikilink1" title="geschlechtsneutrale_sprache">hier zu finden</a></span>)
            nicht vorkommen und nur durch das (Satz-)Zeichen inkludiert werden.
        </p>

