    <h2 class="sectionedit2" id="ziele_und_ansprüche">Goals and aspirations</h2>
    <div class="level2">

        <p>
            The key goals are:
        </p>
        <ul>
            <li class="level1 node">
                <div class="li">
                    Alternatives to the perceived masculine and feminine forms of
                    nouns to include non-binary individuals.
                </div>
                <ul>
                    <li class="level2">
                        <div class="li"> For example, alternatives to <span class="tip" data-bs-toggle="tooltip" title="clerk (male)">Verkäufer</span>,
                            <span class="tip" data-bs-toggle="tooltip" title="clerk (female)">Verkäuferin</span>,
                            <span class="tip" data-bs-toggle="tooltip" title="current common notations to include women and men">Verkäufer/-in und VerkäuferIn</span>.
                        </div>
                    </li>
                </ul>
            </li>
            <li class="level1">
                <div class="li">
                    Grammatical words and endings that are interpreted as neither masculine nor feminine, like alternatives to
                    <span class="tip" data-bs-toggle="tooltip" title="&quot;the&quot;, but gendered">„der/die“</span>,
                    <span class="tip" data-bs-toggle="tooltip" title="&quot;a&quot;, but gendered">„ein/eine“</span>, „gute/r“, „nette/n“ in statements like
                    <span class="tip" data-bs-toggle="tooltip" title="the student, male or female">„der/die Schüler/in“</span>,
                    <span class="tip" data-bs-toggle="tooltip" title="a good friend, male or female">„ein/eine gute/r Freund/in“</span>,
                    <span class="tip" data-bs-toggle="tooltip" title="a nice clerk, male or female">„den/die nette/n Verkäufer/in“</span>.
                    <a href="https://nibi.space/vergleich_geschlechtsneutraler_deklinationssysteme" class="wikilink1">This page</a> compares different proposals in that regard.
                </div>
            </li>
        </ul>

        <p>
            However, other aspirations can sometimes get in the way:
        </p>
        <ul>
            <li class="level1">
                <div class="li"> Noticeability: many non-binary people want the form to be impossible to ignore. A common request is that it should "disrupt the flow of reading" to make the reader reflect.
                    <span class="tip" data-bs-toggle="tooltip" title="This, however, is a big issue for acceptance in the public, and is one of the most commonly used counterpoints to gender-inclusive language.">[note]</span>
                </div>
            </li>
            <li class="level1 node">
                <div class="li"> Low barriers:</div>
                <ul>
                    <li class="level2">
                        <div class="li">
                            Many of the suggestions presented here make it more difficult or impossible for people with various disabilities,
                            learning difficulties, and <a
                                href="https://nibi.space/neurodivergenz" class="wikilink1" title="neurodivergenz">neurodivergences</a>
                            to read and understand a text. This is especially true for forms that confuse, give pause, or simply disrupt the flow of reading.
                        </div>
                    </li>
                    <li class="level2">
                        <div class="li">
                            Especially in case of punctuation marks and special characters, People who use <a
                                href="https://en.wikipedia.org/wiki/Screenreader" class="urlextern"
                                title="https://en.wikipedia.org/wiki/Screenreader" rel="nofollow">screen readers</a>
                            need to rely on the software and how <a href="https://nibi.space/text_to_speech" class="wikilink1"
                                                                    title="text_to_speech"> these programs read them out.</a>
                        </div>
                    </li>
                </ul>
            </li>
        </ul>

