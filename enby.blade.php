    <h2 class="sectionedit28" id="vii">Vii</h2>
    <div class="level2">

        <p>
            Basierend auf den Pronomem <a href="https://nibi.space/pronomen#vii" class="urlextern"
                                          title="https://nibi.space/pronomen#vii" rel="nofollow">vii/ vis/ viiv/ vii</a>
            lassen sich auch Artikel und Personalendungen bilden. <sup><a href="#fn__3" id="fnt__3"
                                                                          class="fn_top">3)</a></sup> Ebenso wurde ein
            passender Plural entwickelt, sodass theoretisch die gesamte deutsche Sprache damit ausgedrückt werden
            könnte.
        </p>
        <div class="table sectionedit29">
            <table class="inline">
                <tr class="row0">
                    <th class="col0"> Kasus</th>
                    <td class="col1"> Singular</td>
                    <td class="col2"> Plural</td>
                </tr>
                <tr class="row1">
                    <th class="col0"> Nom</th>
                    <td class="col1">vii</td>
                    <td class="col2"> viis</td>
                </tr>
                <tr class="row2">
                    <th class="col0"> Gen</th>
                    <td class="col1"> vis</td>
                    <td class="col2"> visis</td>
                </tr>
                <tr class="row3">
                    <th class="col0"> Dat</th>
                    <td class="col1"> viiv</td>
                    <td class="col2"> viivs</td>
                </tr>
                <tr class="row4">
                    <th class="col0"> Akk</th>
                    <td class="col1"> vii</td>
                    <td class="col2"> viis</td>
                </tr>
            </table>
        </div>

        <p>
            Personalendungen sind dabei identisch mit den Pronomen selbst, bestimmten Artikeln wird ein „d“ vorgesetzt.
            Relativpronomen sind gleich den Artikeln bzw. dem Wort „welch-“ mit der dem Kasus entsprechenden
            Personalendung. Also welchvii(N), welchviiv(D), welchvii(A) im Singular und welchviis(N), welchviivs(D) und
            welchviis(A) im Plural.
        </p>

        <p>
            Aus Übereifer wurden dabei ebenfalls Artikel für den Plural entworfen, auch wenn diese eigentlich nicht
            nötig sind, da Pluralartikel bereits geschlechtsunspezifisch sind. Der Vollständingkeit und Einheitlichkeit
            halber wurden diese dann aber bisher beibehalten.
        </p>

        <p>
            Anzumerken ist, das bei diesem Konzept das „er“ am Ende eines Wortes ersetzt wird, anstatt die neutrale
            Endung hinten anzuhängen, um zu vermeiden, das vermeintlich neutrale Begrife noch einen männlichen Stamm
            enthalten.
        </p>

        <p>
            Fahrer wird also nicht zu Fahrervii sonder direkt zu Fahrvii. Mitarbeiterin nicht zu Mitarbeitervii, sondern
            zu Mitarbeitvii.
        </p>

