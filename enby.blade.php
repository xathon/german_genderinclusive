    <h2 class="sectionedit20" id="die_sylvain-konventionen">Die SYLVAIN-Konventionen</h2>
    <div class="level2">

        <p>
            Bei den SYLVAIN-Konventionen wird das Indefenitivum zu den üblichen drei grammatikalischen Geschlechtern
            hinzugefügt. <sup><a href="#fn__2" id="fnt__2" class="fn_top">2)</a></sup>
        </p>

        <p>
            Für nichtbinäre Leute oder für Menschen, von denen das Gender unbekannt ist, gibt es folgende Wortendungen:
        </p>
        <ul>
            <li class="level1">
                <div class="li"><strong>din</strong> Student<strong>nin</strong></div>
            </li>
            <li class="level1">
                <div class="li"> mein<strong>in</strong> best<strong>in</strong> Freund<strong>nin</strong></div>
            </li>
            <li class="level1">
                <div class="li"> ein<strong>in</strong> gut<strong>in</strong> Leser<strong>nin</strong></div>
            </li>
        </ul>

        <p>
            Die Mehrzahl wird ähnlich wie das Femininum gebildet.
        </p>
        <ul>
            <li class="level1">
                <div class="li"> die Lehrer<strong>ninnen</strong></div>
            </li>
            <li class="level1">
                <div class="li"> die Schüler<strong>ninnen</strong></div>
            </li>
        </ul>

        <p>
            Zur Inklusion aller Gender wird das Binnen-N vorgestellt.
        </p>
        <ul>
            <li class="level1">
                <div class="li"> die Arbeiter<strong>N</strong>innen</div>
            </li>
            <li class="level1">
                <div class="li"> die Freund<strong>N</strong>innen</div>
            </li>
        </ul>

        <p>
            Gän kann sich <a href="http://www.liminalis.de/2008_02/Liminalis-2008-Sylvain-Balzer.pdf" class="urlextern"
                             title="http://www.liminalis.de/2008_02/Liminalis-2008-Sylvain-Balzer.pdf" rel="nofollow">hier</a>
            mehr über die SYLVAIN-Konventionen und das geschlechtsneutrale Personalpronomen „nin“ informieren.
        </p>

